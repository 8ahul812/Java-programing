package Basic;

public class Divided {
    public static void main(String[] args) {
        double a = 5;
        double b = 2;
        double z = a/b;
        System.out.println("z="+z);
        double x = 5/2;
        System.out.println("x="+x);
        double y = 5.0/2.0;
        System.out.println("y="+y);
        double v = 5.0/2;
        System.out.println("v="+v);
        double w = 5/2.0;
        System.out.println("w="+w);
        double d = 2;
        System.out.println("d="+d);

    }
}


